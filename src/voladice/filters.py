def GREATER(roll: int, target: int) -> bool:
    return roll > target

def LESS(roll: int, target: int) -> bool:
    return roll < target

def EQUAL(roll: int, target: int) -> bool:
    return roll == target

def DIFFERENT(roll: int, target: int) -> bool:
    return roll != target

def GREATER_EQUAL(roll: int, target: int) -> bool:
    return roll >= target

def LESS_EQUAL(roll: int, target: int) -> bool:
    return roll <= target
